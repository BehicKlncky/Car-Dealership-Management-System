    private void sliderMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_sliderMouseDragged
        try 
        {
            int value = slider.getValue();
            if (jLabel26.getText().equals("Honda Accord"))
            {
                if (color_gri.isSelected() && value != 0 && value < 10)
                {
                    String icon_name = "/main/images/AccordResized/" + "accord-0" + value + ".png";
                    lbl_honda.setIcon(new javax.swing.ImageIcon(getClass().getResource(icon_name)));
                }
                else if (color_gri.isSelected() && value != 0 && value >= 10)
                {
                    String icon_name = "/main/images/AccordResized/" + "accord-" + value + ".png";
                    lbl_honda.setIcon(new javax.swing.ImageIcon(getClass().getResource(icon_name)));
                }
            }
            else
            {
                if (color_beyaz.isSelected() && value != 0)
                {
                    String icon_name = "/main/images/HondaResized/" + "beyaz-" + value + ".png";
                    lbl_honda.setIcon(new javax.swing.ImageIcon(getClass().getResource(icon_name)));
                }
                else if (color_gumus.isSelected() && value != 0)
                {
                    String icon_name = "/main/images/HondaResized/" + "gumus-" + value + ".png";
                    lbl_honda.setIcon(new javax.swing.ImageIcon(getClass().getResource(icon_name)));
                }
                else if (color_gri.isSelected() && value != 0)
                {
                    String icon_name = "/main/images/HondaResized/" + "gri-" + value + ".png";
                    lbl_honda.setIcon(new javax.swing.ImageIcon(getClass().getResource(icon_name)));
                }
                else if (color_kirmizi.isSelected() && value != 0)
                {
                    String icon_name = "/main/images/HondaResized/" + "kirmizi-" + value + ".png";
                    lbl_honda.setIcon(new javax.swing.ImageIcon(getClass().getResource(icon_name)));
                }
                else if (color_mavi.isSelected() && value != 0)
                {
                    String icon_name = "/main/images/HondaResized/" + "mavi-" + value + ".png";
                    lbl_honda.setIcon(new javax.swing.ImageIcon(getClass().getResource(icon_name)));
                }
                else if (color_siyah.isSelected() && value != 0)
                {
                    String icon_name = "/main/images/HondaResized/" + "siyah-" + value + ".png";
                    lbl_honda.setIcon(new javax.swing.ImageIcon(getClass().getResource(icon_name)));
                }
            }
        } 
        catch (Exception e) 
        {
            
        }

    }//GEN-LAST:event_sliderMouseDragged

